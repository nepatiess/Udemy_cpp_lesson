#include <iostream>

using namespace std;
int main() {

	/*
	The for loop is basically a block of code for a certain number of 
		and it is a loop that is used to run on top of each other.
	*/

	// Code a for loop that prints all the numbers from 1 to 10 on the screen.

	for (int i = 1; i <= 10; i++) {
		// transactions: 1, 2, 3, 4...
		cout << i << " ";
	}

	cout << endl;

	return 0;
}